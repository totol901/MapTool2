BOOL CMapTool2App::OnIdle(LONG lCount)
{
	CD3dClass::GetInstance()->GetMapScene()->InputFrame();
	// TODO: Add your specialized code here and/or call the base class
	if (this->m_pMainWnd->IsIconic())
	{
		return FALSE;
	}
	else
	{
		//메인 신 Render
		CD3dClass::GetInstance()->BeginMainScene(0.0f, 0.0f, 1.0f, 1.0f);
		
		CD3dClass::GetInstance()->GetMapScene()->Frame();
		
		CD3dClass::GetInstance()->EndMainScene();

		//미니맵 신 Render
		CD3dClass::GetInstance()->BeginMiniMapScene(0.0f, 1.0f, 1.0f, 1.0f);

		CD3dClass::GetInstance()->GetMiniMapScene()->Frame();
		
		CD3dClass::GetInstance()->EndMiniMapScene();
		
		//속성 신 Render
		CD3dClass::GetInstance()->BeginPropertyScene(0.0f, 1.0f, 0.0f, 1.0f);
		
		CD3dClass::GetInstance()->EndPropertyScene();
	}

	return TRUE;
}
